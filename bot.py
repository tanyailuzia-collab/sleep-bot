logger = logging.getLogger(__name__)

claude_client = anthropic.Anthropic(api_key=CLAUDE_API_KEY)

# Зберігаємо історію розмов (chat_id → список повідомлень)
conversation_history: dict = {}

async def handle_message(update: Update, context: ContextTypes.DEFAULT_TYPE):
    chat_id = update.effective_chat.id
    user_text = update.message.text

    # Показуємо що бот "друкує"
    await context.bot.send_chat_action(chat_id=chat_id, action="typing")

    # Ініціалізуємо історію для нового чату
    if chat_id not in conversation_history:
        conversation_history[chat_id] = []

    # Додаємо повідомлення користувача в історію
    conversation_history[chat_id].append({
        "role": "user",
        "content": user_text
    })

    # Обмежуємо історію до 10 останніх повідомлень (5 туди-назад)
    if len(conversation_history[chat_id]) > 10:
        conversation_history[chat_id] = conversation_history[chat_id][-10:]

    try:
        response = claude_client.messages.create(
            model="claude-haiku-4-5-20251001",
            max_tokens=1024,
            system=SYSTEM_PROMPT,
            messages=conversation_history[chat_id]
        )
        bot_reply = response.content[0].text

        # Зберігаємо відповідь бота в історії
        conversation_history[chat_id].append({
            "role": "assistant",
            "content": bot_reply
        })

    except Exception as e:
        logger.error(f"Помилка Claude API: {e}")
        bot_reply = f"❌ Помилка (для налагодження): {str(e)[:300]}"

    await update.message.reply_text(bot_reply)


async def handle_start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    await update.message.reply_text(
        "Привіт! 👋 Я помічник консультанта з дитячого сну.\n\n"
        "Опишіть вашу ситуацію — і я постараюся допомогти 🌙"
    )


def main():
    print("🤖 Запускаю бота...")
    app = Application.builder().token(TELEGRAM_TOKEN).build()

    from telegram.ext import CommandHandler
    app.add_handler(CommandHandler("start", handle_start))
    app.add_handler(MessageHandler(filters.TEXT & ~filters.COMMAND, handle_message))

    print("✅ Бот працює! Натисни Ctrl+C щоб зупинити.")
    app.run_polling()


if __name__ == "__main__":
    main()
